	  case 0x64: will_printf(t, "1222"); break;
	  case 0x66: will_printf(t, "12212424"); break;
	  case 0x28: will_printf(t, "112"); break;
	  case 0xA8: will_printf(t, "1111122222"); break;

	  case 0x4C: case 0x67: case 0x70: case 0x78:
